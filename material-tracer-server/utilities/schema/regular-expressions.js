var regEx = {
  alphaWord: /^[a-z]+$/,
  email: /^(([^<>()\[\]\\.,;:\s@"]+(\.[^<>()\[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/,
  password:/^[a-z0-9@#\$%]*$/,
  mobile: [
    /^(9|8|7|6)(\d){9}$/,
    /^(9|8|7|6)(\d){2}[-. ](\d){3}[-. ](\d){4}$/,

    /^[\+]?[9][1](9|8|7|6)(\d){2}(\d){3}(\d){4}$/,
    /^[\+]?[9][1][-. ](9|8|7|6)(\d){2}[-. ](\d){3}[-. ](\d){4}$/
  ],
  latitude: /^[-|+]?((([0-9]|([1-8][0-9]))(\.[0-9]{0,100})?)|(90))\°?$/,
  longitude: /^[-|+]?((([0-9]|([1-9][0-9])|(1[0-7][0-9]))(\.[0-9]{0,100})?)|(180))\°?$/,
  integer: /^[-|+]?\d+$/,
  positiveInteger: /^[+]?\d+$/,
  positiveIntegerNoPreZero: /^[+]?[1-9][0-9]*$/,
  alphaString: /^[a-z\s]+$/

};

module.exports = regEx;
